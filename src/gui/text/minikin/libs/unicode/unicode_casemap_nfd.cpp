const int g_nfdMappingsCount = sizeof(g_nfdMappings) / sizeof(g_nfdMappings[0]);

const NfdMapping* findNfdMapping(UChar32 c) {
    int lo = 0, hi = g_nfdMappingsCount - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        uint32_t v = g_nfdMappings[mid].cp;
        if (v == (uint32_t)c) return &g_nfdMappings[mid];
        if (v < (uint32_t)c) lo = mid + 1;
        else hi = mid - 1;
    }
    return nullptr;
}
